#pragma once

namespace FL
{

//----------------------------------------------------------------------------
// Clipboard
//----------------------------------------------------------------------------

//! This class represents system clipboard.
class Clipboard
{
public:
	//! Copy string to clipboard.
	//! @param		string		The string to be copied to clipboard.
	//! @return		True indicates success, false indicates failure.
	static _bool Copy( StringPtr string );

	//! Paste string from clipboard.
	//! @param		string		The string to be paste from clipboard.
	//! @return		True indicates success, false indicates failure.
	static _bool Paste( String& string );
};

};